
#{}  s'utilise pour utiliser dans un meme valeur des entieres plus strings

puts "On va compter le nombre d'heures de travail à THP"   #affiche ce qui a ecrit
puts "Travail : #{10 * 5 * 11}"        # affiche le texte et fait la multiplication (sting + entieres) a l'interieure du tableau  550h
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"    # affiche le texte ey fait la multiplication (sting + entieres)a l'interieure du tableau 33000s

puts "Et en secondes ?"  #affiche ce qui a ecrit

puts 10 * 5 * 11 * 60 * 60   #fait la multiplication des entieres

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"      #affiche ce qui a ecrit

puts 3 + 2 < 5 - 7                                     # fait un boolean par rapport au dernier put, il n'est pas vrai, donc faux

puts "Ça fait combien 3 + 2 ? #{3 + 2}"     #affiche le texte et l'operation
puts "Ça fait combien 5 - 7 ? #{5 - 7}" #affiche le texte et l'operation

puts "Ok, c'est faux alors !"     #affiche le texte

puts "C'est drôle ça, faisons-en plus :"    #affiche le texte

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"    #affiche le texte plus le boolean
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"   #affiche le texte plus le boolean
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"   #affiche le texte plus le boolean
